class Product:

    def __init__(self,name, price):
        self.name = name
        self.price = price

    def getName(self):
        return self.name

    def getPrice(self):
        return self.price

    def fullInfo(self):
        return f'Название продукта: {self.name}, цена продукта: {self.price}'





























