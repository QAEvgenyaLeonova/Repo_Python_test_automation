'''
Selenium — это робот для браузера.
Он делает то, что делаешь ты: кликает, печатает, переходит по ссылкам.
Помогает быстро проверять сайты и находить ошибки.
Состоит из частей: водитель (WebDriver), помощник по установке (Manager), команда роботов (Grid), кнопка записи (IDE) и переводчики (драйверы).
Получается, Selenium — это как армия маленьких помощников, которые тестируют сайты вместо людей. Хочешь, разберу какой‑то кусочек ещё подробнее?
'''

#1.Написание первого теста
from selenium import webdriver

# Инициализация драйвера (Selenium Manager сделает всё автоматически)
driver = webdriver.Chrome()

#Открывам сайт
driver.get("https://www.python.org")

#Здесь можно добавить проверки(asser), поиск элементов и т.д

#Закрываем браузер
driver.quit()


