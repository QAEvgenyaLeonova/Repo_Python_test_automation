#2. Параметризация фикстуры:
@pytest.fixture(params=[
    {"url": "https://example.com/api/v1/users", "expected": 200},
    {"url": "https://example.com/invalid", "expected": 404},
])#@pytest.fixture — говорит Pytest: «Создай мне помощника (фиксатуру), который будет выполнять одни и те же действия для разных данных».params — это список заданий для «почтальона»:
def api_request(request):#. Функция api_request — сам «почтальон»
    data = request.param #data = request.param — берёт одно задание из списка (params). Сначала — первое, потом — второе.
    response = requests.get(data["url"]) #идёт по указанному URL (как будто нажимает ссылку в браузере) и получает ответ от сервера.
    data["response"] = response #кладёт полученный ответ (статус, данные и т. д.) в то же задание.
    return data #return data — отдаёт заполненное задание «контролёру».

def test_api_status(api_request): #Тест test_api_status — «контролёр» Получает заполненное задание от «почтальона» (api_request).
    response = api_request["response"] #response — что ответил сервер (например, 200 или 404);
    expected = api_request["expected"] #expected — какой статус мы ожидали (указан в задании).
    assert response.status_code == expected #Сравнивает: assert response.status_code == expected.
#Фикстура выполняет запрос заранее.
#Логика запроса вынесена в одно место.
