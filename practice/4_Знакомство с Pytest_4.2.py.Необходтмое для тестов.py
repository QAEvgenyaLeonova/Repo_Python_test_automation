#12. Проверка предупреждений (warnings)
#Представь, что программа — это твой друг, который иногда говорит: «Эй, тут кое‑что не очень хорошо, но я всё равно продолжу работать». Такие сообщения и есть предупреждения (warnings).
import warnings #warnings — модуль Python для создания и управления предупреждениями;
import pytest #pytest — инструмент для написания тестов (проверок) кода.

def deprecated_function():#функция deprecated_function() говорит: «Я устарела, лучше меня не использовать!»;
    warnings.warn('Функция устарела', DeprecationWarning)
    return 42 #return нужен, чтобы функция что‑то отдавала после выполнения. Без него она вернёт None.- то есть можно поставить что угодно

def test_deprecated_warning():#тест test_deprecated_warning() проверяет, что это предупреждение действительно появляется.
    with pytest.warns(DeprecationWarning):
        deprecated_function()
'''Представь, что у тебя есть старая игрушка, которая ещё работает, но может сломаться. Ты говоришь другу:
«Осторожно, эта машинка уже старая, может сломаться!»
Тест проверяет, сказал ли ты эту фразу. Если сказал — всё правильно. Если не сказал — тест ругается: «Ты забыл предупредить!»
warnings.warn()	Показывает предупреждение	warnings.warn("Текст", тип)
DeprecationWarning	Тип предупреждения для устаревших функций	«Эта функция устарела»
pytest.warns()	Проверяет, что предупреждение появилось	with pytest.warns(тип):
'''




