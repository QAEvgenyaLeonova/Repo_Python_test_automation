'''
Обычная параметризация (@pytest.mark.parametrize)
Суть: позволяет запустить один и тот же тест с разными наборами входных данных.
Как работает:
Декоратор @pytest.mark.parametrize принимает:
строку с именами параметров (через запятую);
список кортежей — каждый кортеж это набор значений для одного запуска теста.
Тест выполняется столько раз, сколько кортежей в списке.
'''
import pytest

@pytest.mark.parametrize("a, b, expected", [
    (1, 2, 3),
    (0, 0, 0),
    (-1, 1, 0),
    (10, 20, 30),
])
def test_add(a, b, expected):
    assert a + b == expected
'''
Когда использовать:
Когда нужно проверить одну функцию на разных входных данных.
Для тестов с простыми данными (числа, строки, списки).
Когда логика теста не меняется, меняются только данные.
'''


'''
Параметризация фикстур (@pytest.fixture(params=...))
Суть: создаёт несколько версий фикстуры — каждая со своим набором данных. Тесты, использующие эту фикстуру, будут запущены для каждой её версии.
Как работает:
В декораторе @pytest.fixture передаётся параметр params — список значений или кортежей.
Фикстура выполняется столько раз, сколько элементов в params.
Внутри фикстуры можно получить текущий параметр через request.param.
Каждый тест, использующий эту фикстуру, запускается для каждого значения из params.
'''
import pytest

@pytest.fixture(params=[
    {"username": "admin", "password": "secret"},
    {"username": "user", "password": "12345"},
    {"username": "guest", "password": ""},
])
def user_credentials(request):
    return request.param

def test_login(user_credentials):
    # Тест запустится 3 раза — по одному для каждого набора credentials
    username = user_credentials["username"]
    password = user_credentials["password"]

    # Здесь логика теста авторизации
    print(f"Тестируем логин: {username}")
    assert len(password) > 0  # Упрощённая проверка
'''
Когда использовать:
Когда тестовые данные сложные (словари, объекты, конфигурации).
Когда перед тестом нужно выполнить сложную настройку (например, создать пользователя в БД, получить токен).
Когда один набор данных нужен сразу в нескольких тестах.
Когда данные для теста зависят от внешних факторов (окружения, API и т. п.).
'''

