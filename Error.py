#pytest --q
import pytest

#1. Простая проверка: «ожидаю ошибку»
def test_zero_division():
    with pytest.raises(ZeroDivisionError):
        1 / 0



#2. Проверка детали: «хочу посмотреть на ошибку поближе»
def test_recursion_depth():
    with pytest.raises(RuntimeError) as excinfo:
        def f():
            f()  # Бесконечная рекурсия — вызовет ошибку
        f()
    assert "maximum recursion" in str(excinfo.value)



# 3.Проверка по шаблону (через регулярное выражение)
'''Если текст ошибки может немного меняться, но в нём точно должно быть какое‑то слово или цифра, используют параметр match.'''
def myfunc():
    raise ValueError('Exception 123 raised')

def test_match():
    with pytest.raises(ValueError, match=r'.* 123 .*'):#Если бы написали match="123", это тоже сработало бы — pytest ищет подстроку.
        myfunc()




# 4.Альтернативный синтаксис: вызов функции напрямую
'''Можно не писать with, а сразу передать функцию и её аргументы в pytest.raises.'''
'''pytest.raises(ValueError, myfunc)'''
#Это эквивалентно
'''with pytest.raises(ValueError):
    myfunc()'''
#Но первый вариант короче, если функция простая.


# 5. Маркировка «ожидаемого провала»
'''(@pytest.mark.xfail)
Иногда тест заведомо должен упасть из‑за бага или незавершённой функции. Чтобы он не «портил статистику», его помечают как «я знаю, что он упадёт».'''
@pytest.mark.xfail(raises=IndexError)
def test_f():
    f()  # Ожидается, что здесь возникнет IndexError
#Этот способ не для проверки логики кода, а для документирования: «Здесь есть проблема, мы про неё знаем».


def f():
    raise SystemExit(1)

def test_mytest():
    with pytest.raises(SystemExit):
        f()
'''
Главные правила
Используйте pytest.raises, когда хотите проверить, что код правильно реагирует на ошибки (например, проверяет входные данные).

Используйте @pytest.mark.xfail, когда документируете известные проблемы (не для обычных тестов).

Параметр match удобен, если текст ошибки может меняться, но ключевые слова остаются.

Переменная excinfo нужна, если требуется детальная проверка (например, анализ кода ошибки).'''






















