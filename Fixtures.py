'''Фикстура — это функция, которая подготавливает окружение для теста: создаёт данные, запускает сервисы, 
настраивает окружение и т. д. Фикстуры автоматически подключаются к тестам через параметр функции.'''
#1. capsys — захват вывода в консоль (stdout/stderr)
#Используется для проверки того, что функция что‑то печатает в консоль.
def test_print_output(capsys):#объявление тестовой функции. Имя test_... говорит Pytest, что это тест.
    print('Hello, world!')
    captured = capsys.readouterr()#capsys.readouterr() — метод, который читает всё, что было выведено в stdout и stderr с момента начала теста.captured — переменная, куда мы сохраняем результат перехвата.
    assert captured.out == 'Hello, world!\n' #captured.out — то, что реально напечатала функция print() (перехвачено capsys).
#capsys — встроенный фикстур (fixture) Pytest. Он позволяет перехватывать:
#вывод в консоль (stdout — стандартный поток вывода);
#ошибки (stderr — стандартный поток ошибок).

#2. caplog — работа с логами
#Позволяет перехватывать логи, генерируемые во время теста.
import logging

def test_logging(caplog):#caplog передаётся как аргумент функции — это специальный механизм Pytest. Когда Pytest видит параметр с именем caplog, он автоматически подставляет соответствующий фикстур.
    logging.warning('Предупреждение!')
    assert 'Предупреждение!' in caplog.text

#3. monkeypatch — временное изменение объектов
#Позволяет «заменять» функции, методы, атрибуты на время теста. Полезно для мокирования.
#Замена атрибута:
#Вот что делает monkeypatch: надевает «парик» на объекты во время теста, а после теста всё возвращает как было.
def test_monkeypatch_attr(monkeypatch):#monkeypatch — специальный инструмент Pytest, который передаётся в тест автоматически. Это «волшебный набор для переодеваний».
    class MyClass:
        value = 10

    monkeypatch.setattr(MyClass, 'value', 20)#monkeypatch.setattr() — команда «надеть парик».
    assert MyClass.value == 20

#Замена функции:
import os #Импортируем модуль os — это набор инструментов для работы с операционной системой (файлами, папками, путями и т. д.).

def test_monkeypatch_func(monkeypatch):#monkeypatch — специальный инструмент Pytest, который даёт нам ту самую «волшебную кнопку» для замены функций.
    def mock_getcwd():#Создаём поддельную (моковую) функцию mock_getcwd.Она не делает ничего сложного — просто всегда возвращает строку /mocked/path
        return '/mocked/path'

    monkeypatch.setattr(os, 'getcwd', mock_getcwd)#os — модуль, в котором хотим что‑то поменять;'getcwd' — имя функции, которую хотим заменить;mock_getcwd — наша поддельная функция, которая встанет на место настоящей.
    assert os.getcwd() == '/mocked/path'

#4. tmp_path — временные файлы и директории
#Создаёт уникальную временную директорию для каждого теста (на базе pathlib.Path).
def test_temp_file(tmp_path):#Песочница = временная директория (tmp_path).Pytest автоматически создаёт для этого теста уникальную временную папку и передаёт её в функцию под именем tmp_path
    # Создаём временный файл
    file = tmp_path / "test.txt"#tmp_path — путь к нашей временной папке (песочнице)./ 'test.txt' — оператор / соединяет путь и имя файла. Получается полный путь к файлу внутри временной папки.file — переменная, в которой хранится этот путь.
    file.write_text("Содержимое файла")#file.write_text() — метод, который записывает текст в файл.'Содержимое файла' — текст, который мы хотим записать.

    # Проверяем содержимое
    assert file.read_text() == "Содержимое файла"



